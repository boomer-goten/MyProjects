-- Основное отличие SERIALIZABLE от READ COMMITTED которое мы видим в том что
-- Serializable — буду хранить локальную копию бд и не буду видеть изменений, 
-- которые вносит другой пользователь, пока ты не закоммитишь, а при коммите кто первый, тот и прав.
-- Тоесть изменения от паралелльной транзакции только после завершения своей.